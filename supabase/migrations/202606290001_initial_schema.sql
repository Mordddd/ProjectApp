create or replace function public.has_feature(requested_feature text)
returns boolean
language sql
stable
security definer
set search_path = ''
as $$
  select coalesce(
    (select up.allowed
       from public.user_permissions up
      where up.user_id = auth.uid() and up.feature_code = requested_feature),
    (select rp.allowed
       from public.role_permissions rp
       join public.profiles p on p.role_id = rp.role_id
      where p.user_id = auth.uid()
        and p.account_status = 'active'
        and rp.feature_code = requested_feature),
    false
  );
$$;

revoke all on function public.has_feature(text) from public;
grant execute on function public.has_feature(text) to authenticated;

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
declare
  general_division_id bigint;
  new_username text;
begin
  select id into general_division_id from public.divisions where code = 'GEN';
  new_username := lower(coalesce(
    nullif(new.raw_user_meta_data ->> 'username', ''),
    split_part(new.email, '@', 1)
  ));

  insert into public.profiles (
    user_id, username, full_name, display_name, division_id, role_id
  ) values (
    new.id,
    new_username,
    coalesce(new.raw_user_meta_data ->> 'full_name', ''),
    coalesce(new.raw_user_meta_data ->> 'display_name', ''),
    general_division_id,
    4
  );
  return new;
end;
$$;

create trigger on_auth_user_created
after insert on auth.users
for each row execute function public.handle_new_user();

create or replace function public.protect_profile_security_fields()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  if auth.uid() = old.user_id and not public.has_feature('access_control') then
    new.role_id = old.role_id;
    new.account_status = old.account_status;
    new.username = old.username;
  end if;
  return new;
end;
$$;

create trigger profiles_protect_security_fields
before update on public.profiles
for each row execute function public.protect_profile_security_fields();

