create extension if not exists citext with schema extensions;

create table public.roles (
  id smallint primary key,
  code text not null unique,
  name text not null unique
);

create table public.divisions (
  id bigint generated by default as identity primary key,
  code text not null unique,
  name text not null,
  created_at timestamptz not null default now()
);

create table public.features (
  code text primary key,
  name text not null,
  description text not null default ''
);

create table public.profiles (
  user_id uuid primary key references auth.users(id) on delete cascade,
  legacy_id bigint generated by default as identity unique not null,
  username extensions.citext not null unique,
  full_name text not null default '',
  display_name text not null default '',
  bio text not null default '',
  hobby text not null default '',
  nik text not null default '',
  address text not null default '',
  phone text not null default '',
  contact_email text not null default '',
  education text not null default '',
  avatar_path text,
  video_path text,
  division_id bigint references public.divisions(id) on delete set null,
  role_id smallint not null default 4 references public.roles(id),
  account_status text not null default 'active'
    check (account_status in ('active', 'blocked', 'departed')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint username_format check (username::text ~ '^[a-z0-9._-]+$')
);

create table public.role_permissions (
  role_id smallint not null references public.roles(id) on delete cascade,
  feature_code text not null references public.features(code) on delete cascade,
  allowed boolean not null default false,
  updated_at timestamptz not null default now(),
  primary key (role_id, feature_code)
);

create table public.user_permissions (
  user_id uuid not null references public.profiles(user_id) on delete cascade,
  feature_code text not null references public.features(code) on delete cascade,
  allowed boolean not null,
  updated_at timestamptz not null default now(),
  primary key (user_id, feature_code)
);

create table public.zodiac_records (
  id bigint generated by default as identity primary key,
  owner_id uuid not null references public.profiles(user_id) on delete cascade,
  day smallint not null check (day between 1 and 31),
  month smallint not null check (month between 1 and 12),
  zodiac_name text not null,
  date_range text not null,
  character text not null default '',
  strengths text not null default '',
  weaknesses text not null default '',
  compatible text not null default '',
  less_compatible text not null default '',
  element text not null default '',
  symbol text not null default '',
  accent_value bigint not null default 4279381363,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.quiz_attempts (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references public.profiles(user_id) on delete cascade,
  quiz_code text not null,
  correct_count integer not null check (correct_count >= 0),
  total_count integer not null check (total_count > 0),
  answers jsonb not null default '[]'::jsonb,
  completed_at timestamptz not null default now(),
  check (correct_count <= total_count)
);

create table public.polls (
  id uuid primary key default gen_random_uuid(),
  title text not null,
  question text not null,
  is_active boolean not null default true,
  created_by uuid references public.profiles(user_id) on delete set null,
  created_at timestamptz not null default now()
);

create table public.poll_options (
  id uuid primary key default gen_random_uuid(),
  poll_id uuid not null references public.polls(id) on delete cascade,
  label text not null,
  position smallint not null default 0,
  unique (poll_id, label),
  unique (poll_id, id)
);

create table public.poll_votes (
  poll_id uuid not null references public.polls(id) on delete cascade,
  option_id uuid not null,
  user_id uuid not null references public.profiles(user_id) on delete cascade,
  created_at timestamptz not null default now(),
  primary key (poll_id, user_id),
  foreign key (poll_id, option_id)
    references public.poll_options(poll_id, id) on delete cascade
);

